#!/usr/bin/env python3
"""
Tool Wrapper Generator for Intelligence Blocks
Generates executable wrapper scripts for each block type and registers them.
"""

import sqlite3
import json
import argparse
import logging
import uuid
from pathlib import Path

logging.basicConfig(level=logging.INFO, format="%(asctime)sZ %(levelname)s %(message)s")

# Paths
INTELLIGENCE_DIR = Path("/home/workspace/Intelligence")
BLOCKS_DB = INTELLIGENCE_DIR / "blocks.db"
EXECUTABLES_DB = Path("/home/workspace/N5/data/executables.db")
WRAPPERS_DIR = INTELLIGENCE_DIR / "tools"

def get_all_blocks():
    """Retrieve all production blocks from database."""
    conn = sqlite3.connect(BLOCKS_DB)
    conn.row_factory = sqlite3.Row
    cursor = conn.cursor()
    
    cursor.execute("""
        SELECT block_id, name, category, status, output_format
        FROM blocks
        WHERE status = 'active' AND block_id != 'B99'
        ORDER BY block_id
    """)
    
    blocks = [dict(row) for row in cursor.fetchall()]
    conn.close()
    return blocks

