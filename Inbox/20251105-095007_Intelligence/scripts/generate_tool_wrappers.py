def create_wrapper_files(blocks, dry_run=False):
    """Create wrapper script files for all blocks."""
    WRAPPERS_DIR.mkdir(exist_ok=True, parents=True)
    
    created = []
    for block in blocks:
        block_id = block['block_id']
        name = block['name']
        category = block['category']
        
        # Generate filename (e.g., generate-b01)
        wrapper_name = f"generate-{block_id.lower()}"
        wrapper_path = WRAPPERS_DIR / wrapper_name
        
        if dry_run:
            logging.info(f"Would create: {wrapper_path}")
            continue
        
        # Generate and write script
        script_content = generate_wrapper_script(block_id, name, category)
        with open(wrapper_path, 'w') as f:
            f.write(script_content)
        
        # Make executable
        wrapper_path.chmod(0o755)
        
        created.append({
            'block_id': block_id,
            'name': name,
            'path': str(wrapper_path.absolute()),
            'wrapper_name': wrapper_name
        })
        
        logging.info(f"✓ Created {wrapper_name} ({name})")
    
    return created

def register_in_executables_db(wrappers, dry_run=False):
    """Register all wrappers in N5 executables database."""
    
    if not EXECUTABLES_DB.exists():
        logging.warning(f"Executables database not found: {EXECUTABLES_DB}")
        logging.info("Skipping registration (will need manual registration)")
        return 0
    
    conn = sqlite3.connect(EXECUTABLES_DB)
    cursor = conn.cursor()
    
    registered = 0
    for wrapper in wrappers:
        if dry_run:
            logging.info(f"Would register: {wrapper['wrapper_name']}")
            continue
        
        try:
            tool_id = f"tool_{wrapper['block_id'].lower()}_{uuid.uuid4().hex[:8]}"
            cursor.execute("""
                INSERT OR REPLACE INTO executables (
                    id, name, type, file_path, description, category, tags
                ) VALUES (?, ?, ?, ?, ?, ?, ?)
            """, (
                tool_id,
                wrapper['wrapper_name'],
                'tool',
                wrapper['path'],
                f"Generate {wrapper['name']} ({wrapper['block_id']})",
                'intelligence',
                json.dumps(['intelligence', 'block-generator', wrapper['block_id'].lower()])
            ))
            registered += 1
            logging.info(f"✓ Registered {wrapper['wrapper_name']}")
        except Exception as e:
            logging.error(f"Failed to register {wrapper['wrapper_name']}: {e}")
    
    conn.commit()
    conn.close()
    
    return registered

def main():
    parser = argparse.ArgumentParser(
        description="Generate tool wrappers for all Intelligence blocks"
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Show what would be created without creating files"
    )
    parser.add_argument(
        "--skip-registration",
        action="store_true",
        help="Skip registering in executables.db"
    )
    
    args = parser.parse_args()
    
    try:
        # Get all blocks
        logging.info("Reading blocks from database...")
        blocks = get_all_blocks()
        logging.info(f"Found {len(blocks)} production blocks")
        
        # Create wrapper scripts
        logging.info("\nGenerating wrapper scripts...")
        wrappers = create_wrapper_files(blocks, dry_run=args.dry_run)
        
        if not args.dry_run:
            logging.info(f"\n✓ Created {len(wrappers)} wrapper scripts in {WRAPPERS_DIR}")
        
        # Register in executables database
        if not args.skip_registration and not args.dry_run:
            logging.info("\nRegistering tools in executables database...")
            registered = register_in_executables_db(wrappers, dry_run=args.dry_run)
            logging.info(f"✓ Registered {registered} tools")
        elif args.dry_run:
            logging.info(f"\nWould register {len(wrappers)} tools in executables.db")
        
        # Summary
        logging.info("\n" + "="*60)
        logging.info("SUMMARY")
        logging.info("="*60)
        logging.info(f"Production blocks: {len(blocks)}")
        logging.info(f"Wrappers created: {len(wrappers)}")
        if not args.skip_registration:
            logging.info(f"Tools registered: {registered if not args.dry_run else len(wrappers)}")
        logging.info(f"Location: {WRAPPERS_DIR.absolute()}")
        logging.info("="*60)
        
        return 0
        
    except Exception as e:
        logging.error(f"Error: {e}", exc_info=True)
        return 1

if __name__ == "__main__":
    exit(main())
